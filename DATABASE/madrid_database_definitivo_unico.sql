CREATE TABLE categories (id SMALLINT PRIMARY KEY, slug TEXT UNIQUE NOT NULL, name TEXT NOT NULL);

CREATE TABLE budgets (id SMALLINT PRIMARY KEY, slug TEXT UNIQUE NOT NULL, name TEXT NOT NULL, min_spend_eur NUMERIC(8,2), max_spend_eur NUMERIC(8,2));

CREATE TABLE neighborhoods (id SMALLINT PRIMARY KEY, name TEXT UNIQUE NOT NULL, city TEXT NOT NULL DEFAULT 'Madrid', is_central BOOLEAN NOT NULL DEFAULT TRUE);

CREATE TABLE sources (id SMALLINT PRIMARY KEY, domain TEXT UNIQUE NOT NULL, source_name TEXT NOT NULL, source_type TEXT NOT NULL, base_url TEXT);

CREATE TABLE places (
id BIGINT PRIMARY KEY,
name TEXT NOT NULL,
slug TEXT NOT NULL UNIQUE,
neighborhood_id SMALLINT NOT NULL REFERENCES neighborhoods(id),
description TEXT,
official_website TEXT,
full_address TEXT NOT NULL,
latitude NUMERIC(9,6),
longitude NUMERIC(9,6),
distance_m INTEGER,
distance_km NUMERIC(8,2),
walking_minutes INTEGER,
driving_minutes INTEGER,
is_estimated_distance BOOLEAN NOT NULL DEFAULT TRUE,
is_estimated_travel_time BOOLEAN NOT NULL DEFAULT TRUE,
recommended_for_groups BOOLEAN NOT NULL DEFAULT TRUE,
reservation_needed BOOLEAN NOT NULL DEFAULT FALSE,
dress_code TEXT,
opening_notes TEXT,
rating NUMERIC(3,2),
source_id SMALLINT REFERENCES sources(id),
updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE tags (id SMALLINT PRIMARY KEY, name TEXT UNIQUE NOT NULL);

CREATE TABLE place_tags (place_id BIGINT REFERENCES places(id) ON DELETE CASCADE, tag_id SMALLINT REFERENCES tags(id) ON DELETE CASCADE, PRIMARY KEY(place_id, tag_id));

CREATE TABLE experiences (
id BIGINT PRIMARY KEY,
place_id BIGINT NOT NULL UNIQUE REFERENCES places(id) ON DELETE CASCADE,
category_id SMALLINT NOT NULL REFERENCES categories(id),
budget_id SMALLINT NOT NULL REFERENCES budgets(id),
subcategory TEXT NOT NULL,
short_description TEXT,
average_spend_per_person NUMERIC(8,2),
price_range_text TEXT,
opening_hours_text TEXT,
recommended_days TEXT,
practical_notes TEXT,
source_id SMALLINT REFERENCES sources(id),
last_verified_at DATE NOT NULL
);

CREATE INDEX idx_places_distance ON places(distance_m);

CREATE INDEX idx_experiences_category_budget ON experiences(category_id, budget_id, average_spend_per_person);

CREATE INDEX idx_places_neighborhood ON places(neighborhood_id);

CREATE VIEW v_place_experiences AS
SELECT p.id, p.name, p.slug, c.slug AS category, b.slug AS budget, e.subcategory,
COALESCE(e.short_description, p.description) AS description, p.official_website, p.full_address,
p.latitude, p.longitude, p.distance_m, p.distance_km, p.walking_minutes, p.driving_minutes,
p.is_estimated_distance, p.is_estimated_travel_time, e.average_spend_per_person, e.price_range_text,
e.opening_hours_text, e.recommended_days, p.recommended_for_groups, p.reservation_needed, p.dress_code,
p.opening_notes, p.rating, p.source_id, e.last_verified_at, e.practical_notes
FROM places p
JOIN experiences e ON e.place_id = p.id
JOIN categories c ON c.id = e.category_id
JOIN budgets b ON b.id = e.budget_id;

